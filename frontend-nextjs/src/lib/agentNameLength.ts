function isWideOrFullWidthCodePoint(codePoint: number): boolean {
	return WIDE_CODE_POINT_RANGES.some(
		([start, end]) => codePoint >= start && codePoint <= end,
	);
}

export function getAgentNameDisplayWidth(value: string): number {
	return Array.from(value).reduce((total, char) => {
		const codePoint = char.codePointAt(0) ?? 0;
		return total + (isWideOrFullWidthCodePoint(codePoint) ? 2 : 1);
	}, 0);
}

export function trimToAgentNameMaxDisplayWidth(value: string): string {
	let width = 0;
	let result = "";

	for (const char of Array.from(value)) {
		const codePoint = char.codePointAt(0) ?? 0;
		const charWidth = isWideOrFullWidthCodePoint(codePoint) ? 2 : 1;
		if (width + charWidth > AGENT_NAME_MAX_DISPLAY_WIDTH) break;
		width += charWidth;
		result += char;
	}

	return result;
}
